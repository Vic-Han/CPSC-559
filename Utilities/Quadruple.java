package Utilities;
public class Quadruple<A,B,C,D> { 
    public final A first; 
    public final B second;
    public final C third;
    public final D fourth; 
    public Quadruple(A a, B b, C c, D d) { 
      this.first = a; 
      this.second = b; 
      this.third = c;
      this.fourth = d;
    }
}